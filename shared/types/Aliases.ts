/**
 * A string representing a Beat Saber game version.
 *
 * ```
 * 1.37.0_9064817954
 * ```
 */
export type GameVersion = string;

/**
 * A string representing a mod version
 *
 * ```
 * 1.0.0
 * ```
 */
export type ModVersion = string;

/**
 * A string representing a mod id
 *
 * ```
 * custom-types
 * ```
 */
export type ModId = string;

/**
 * A string representing a sha-1 hash
 *
 * ```
 * 5baa61e4c9b93f3f0682250b6cf8331b7ee68fd8
 * ```
 */
export type Sha1Hash = string;

/**
 * A string representing a url
 *
 * ```
 * https://example.com/
 * ```
 */
export type Url = string;
